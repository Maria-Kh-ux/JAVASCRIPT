// 1. Створюємо константу для поточного року
const CURRENT_YEAR = 2025;

// 2. Запитуємо рік народження (prompt повертає текст, тому ми перетворюємо його на число)
let birthYear = prompt("Введіть рік вашого народження:");

// 3. Рахуємо вік
let age = CURRENT_YEAR - birthYear;

// 4. Виводимо результат
alert("Вам зараз або виповниться цього року: " + age);
